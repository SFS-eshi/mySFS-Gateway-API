var createError = require('http-errors');
var express = require('express');
var path = require('path');
var cookieParser = require('cookie-parser');
var logger = require('morgan');
var bodyParser = require('body-parser');

var indexRouter = require('./routes/index');
var usersRouter = require('./routes/users');
var usaepayRouter = require('./routes/usaepay/usaepay');
var donationsRouter = require('./routes/donations');
var blueRouter = require('./routes/blueHostedToken');
var directPostRouter = require('./routes/directPost');
var hostedTokenRouter = require('./routes/hostedTokenization');
var paymentLinkRouter = require('./routes/paymentLink');


var app = express();

app.use(function(req, res, next) {
  res.header('Access-Control-Allow-Origin', '*');
  res.header('Access-Control-Allow-Headers', 'Origin, X-Requested-With, Content-Type, Accept');
  next();
});
app.use(bodyParser.urlencoded({ extended: true }));

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'pug');

app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', indexRouter);
app.use('/users', usersRouter);
app.use('/usaepay', usaepayRouter);
app.use('/donations', donationsRouter);
app.use('/blue', blueRouter);
app.use('/directPost', directPostRouter);
app.use('/hostedTokenization', hostedTokenRouter);
app.use('/paymentLink', paymentLinkRouter);


// catch 404 and forward to error handler
app.use(function(req, res, next) {
  next(createError(404));
});

// error handler
app.use(function(err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
